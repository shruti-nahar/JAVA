
public class Account1 {
 private int Accno;
 private String Accname;
 private int Accbal;
public Account1(int accno, String accname, int accbal) {
	super();
	Accno = accno;
	Accname = accname;
	Accbal = accbal;
}
public int getAccno() {
	return Accno;
}
public void setAccno(int accno) {
	Accno = accno;
}
public String getAccname() {
	return Accname;
}
public void setAccname(String accname) {
	Accname = accname;
}
public int getAccbal() {
	return Accbal;
}
public void setAccbal(int accbal) {
	Accbal = accbal;
}
 
}
